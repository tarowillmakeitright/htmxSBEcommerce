package com.ecommerce.ecommerce;

public class Score {
    private Double arithmeticGeometricMean;
    private Double arithmeticMean;
    private Double median;

    // Getters and Setters
    public Double getArithmeticGeometricMean() {
        return arithmeticGeometricMean;
    }

    public void setArithmeticGeometricMean(Double arithmeticGeometricMean) {
        this.arithmeticGeometricMean = arithmeticGeometricMean;
    }

    public Double getArithmeticMean() {
        return arithmeticMean;
    }

    public void setArithmeticMean(Double arithmeticMean) {
        this.arithmeticMean = arithmeticMean;
    }

    public Double getMedian() {
        return median;
    }

    public void setMedian(Double median) {
        this.median = median;
    }
}